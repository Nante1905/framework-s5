package genesis;

public class Marque {
    int id;
    String nom;
    String label;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getLabel() {
        return nom;
    }

    public void setLabel(String label) {
        this.label = label;
    }
}
