package genesis;

public class Preference {
    int database;
    int backLangage;
    int frontLangage;

    public int getDatabase() {
        return database;
    }

    public void setDatabase(int database) {
        this.database = database;
    }

    public int getBackLangage() {
        return backLangage;
    }

    public void setBackLangage(int backLangage) {
        this.backLangage = backLangage;
    }

    public int getFrontLangage() {
        return frontLangage;
    }

    public void setFrontLangage(int frontLangage) {
        this.frontLangage = frontLangage;
    }
}
